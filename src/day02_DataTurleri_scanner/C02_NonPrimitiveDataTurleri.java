package day02_DataTurleri_scanner;

public class C02_NonPrimitiveDataTurleri {
    public static void main(String[] args) {
        int sayi = 10;

        String str = "Java candir"; // metinsel ifadeler içerir şimdilik tabi

        System.out.println(sayi); //10

        System.out.println(str.toLowerCase()); // küçük harfle yazdır java candir
        System.out.println(str.toUpperCase()); // büyük hafrle yazdır JAVA CANDİR

        /*
        1- Primitive data türleri sadece değer saklayabilir.
        Non-Primitive data türleri ise değer saklamanın yanında hazır methodlarda barındırır.
        Bu hazır methodlar sayesinde biz tanımlanmış işlemleri kolayca yapabiliriz.

        2- Primitive data türlerinin isimleri küçük harf ile başlar ama
         non primitive data türlerinin isimleri büyük hafrle başlar.
         */

    }
}
