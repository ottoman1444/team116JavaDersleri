package day09_StringMaipulation_forLoop;

public class C08_ForLoopDikkatEdilecekelr {
    public static void main(String[] args) {
        /*
        Verilen ilk deger bitiş şartını sağlamıyorsa
         */

        for (int i = 10; i <0 ; i++) {
            System.out.println(i);
        //Process finished with exit code 0,
            /*
            kod çalışır ama bitiş şartı sağlanmadığı için loop body hiç çalışmaz
             */

            // başlangıç değeri ve artış azalış miktarı ile bitiş şartı hiç sağlanamazsa


        }
        for (int j = 10; j >=0 ; j++) {
            System.out.println(j+" ");

        }

        //sonsuz loop denir ve makinenin rami dolana kadar kod çalışır.





    }
}
